
public class triangle4 {
	public static void main(String[] args)
	{
		System.out.println("4 Triangle");
		for(int i=7; i>=0; i--)
		{
			for(int k=0; k>7; k--)
				 {
				 	System.out.print("");
				 }
			for(int l=7-i; l<7; l++) 
				 {
					 System.out.print("*");
				 }
		System.out.print("\n");
		}
	}

	
}